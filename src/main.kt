class Solution1 {
    fun printBin(num: Double): String {
        // 消暑部分转二进制
        var n = num
        var ans = "0."
        for(i in 1..6){
            n*=2
            if(n<1){
ans+='0'

            }else {
ans+='1'

            }
        }
    }
}